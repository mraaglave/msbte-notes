document.getElementById('branch').addEventListener('change', updateDownloadLink);
document.getElementById('semester').addEventListener('change', updateDownloadLink);

function updateDownloadLink() {
    const branch = document.getElementById('branch').value;
    const semester = document.getElementById('semester').value;
    const downloadButton = document.getElementById('download-button');

    if (branch && semester) {
        const link = syllabusLinks[semester][branch];
        if (link) {
            downloadButton.disabled = false;
            downloadButton.onclick = () => window.open(link, '_blank');
        } else {
            downloadButton.disabled = true;
            downloadButton.onclick = null;
        }
    } else {
        downloadButton.disabled = true;
        downloadButton.onclick = null;
    }
}
